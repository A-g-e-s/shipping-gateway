    public static function getErrorMsg(int $code): string
    {
        if (array_key_exists($code, self::Errors)) {
            $array = self::Errors[$code];
            return sprintf('No.:%s| %s | %s', $code, $array[0], $array[1]);
        }
        return sprintf('No.:%s| chybu se nepodařilo dohledat', $code);
    }
